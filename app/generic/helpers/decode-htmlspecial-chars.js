export default text => {
	let
		map = {
			'&#038;' : '&',
			'&#039;' : '\'',
			'&#8211;': '–',
			'&#8212;': '—',
			'&#8216;': '‘',
			'&#8217;': '’',
			'&#8221;': '”',
			'&#8230;': '…',
			'&amp;'  : '&',
			'&gt;'   : '>',
			'&lt;'   : '<',
			'&quot;' : '"',
		};

	return text.replace(/\&[\w\d\#]{2,5}\;/g, m => map[m]);
}
